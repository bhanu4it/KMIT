 Electronics and Instrumentation Engineering program was introduced in Keshav Memorial Institute of Technology with a sanctioned intake of 0(Discontinued fresh admissions from 2020-21) students in the academic year 2007 – 08. The scope of Instrumentation engineering is vast and appears to be growing due to increased use of automation in manufacturing and process plants. The demand for Instrumentation is growing rapidly and job opportunities for Electronics and instrumentation graduates are many. The Electronics and Instrumentation engineering curriculum includes courses on the design of analog and digital devices used for measurement and control applications. The students also learn to program microcontrollers and PLC’s which finds wide spread application in all disciplines of engineering. Since the commencement of the program, the department has grown with well qualified faculty and infrastructure. The department has well equipped laboratories to meet the curriculum standards.Apart from curriculum, the department has made sincere efforts to bridge the curriculum deficit with current industrial needs by training the students on new technologies such as Cadence tools, Zigbee modules and Microcontrollers in real time applications. The department nurtures the students by encouraging them to actively participate in extracurricular and co-curricular activities.  </p>

